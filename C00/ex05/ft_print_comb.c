#include <unistd.h>

void	ft_print_comb(void)
{
	char	y[3];

	y[0] = 48;
	while (y[0] < 56)
	{
		y[1] = y[0] + 1;
		while (y[1] < 57)
		{
			y[2] = y[1] + 1;
			while (y[2] < 58)
			{
				write(1, y, 3);
				if (y[0] != 55)
					write(1, ", ", 2);
				y[2]++;
			}
			y[1]++;
		}
		y[0]++;
	}
}
