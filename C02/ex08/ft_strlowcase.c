char	*ft_strlowcase(char *str)
{
	char	low;
	int		i;

	low = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += low;
		i++;
	}
	return (str);
}
