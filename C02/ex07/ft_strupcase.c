char	*ft_strupcase(char *str)
{
	char	up;
	int		i;

	up = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= up;
		i++;
	}
	return (str);
}
